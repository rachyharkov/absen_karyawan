        $this->db->like('id', $q);
		$this->db->or_like('users_id', $q);
		$this->db->or_like('tanggal_mulai', $q);
		$this->db->or_like('tanggal_akhir', $q);
		$this->db->or_like('alasan', $q);
		$this->db->or_like('status', $q);
		$this->db->from($this->table);
